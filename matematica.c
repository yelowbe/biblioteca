
#include <math.h>
#include "matematica.h"


    float calcular_seno(float angulo){
        float a = angulo;
        float calcula_seno = sin(a);
        return calcula_seno; 
    }

    float calcular_cosseno(float angulo){
        float b = angulo;
        float calcula_cosseno = cos(b);
        return calcula_cosseno;
    }

    float calcular_tangente(float angulo){
        float c = angulo;
        float calcula_tangente = tan(c);
        return calcula_tangente;
    }


    


